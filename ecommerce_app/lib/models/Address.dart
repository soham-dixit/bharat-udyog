class Address {
  final String name;
  final String mobileNo;
  final String pincode;
  final String country;
  final String state;
  final String city;
  final String house;
  final String area;

  Address({
    required this.name,
    required this.mobileNo,
    required this.pincode,
    required this.country,
    required this.state,
    required this.city,
    required this.house,
    required this.area,
  });
}
